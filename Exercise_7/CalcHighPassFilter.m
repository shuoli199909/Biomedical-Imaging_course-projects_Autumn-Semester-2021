%	TITLE: 
%       CalcHighPassFilter.m				
%								
%	DESCRIPTION:						
%	    Calculate high-pass |u|
%
%	INPUT:								
%       matrix size
%
%	OUTPUT:							
%       filter
%			
%	VERSION HISTORY:						
%	    120216SK INITIAL VERSION 
%	    191020SK UPDATE
%
%=========================================================================

%=========================================================================
%	M A I N  F U N C T I O N
%=========================================================================f
function [filter] = CalcHighPassFilter(matrix)

    % --------------------------------------------------------------------
    % Define high-pass filter according to |u|
    % --------------------------------------------------------------------
    filter = abs(-fix(matrix/2):+fix(matrix/2));
      
    % --------------------------------------------------------------------
    % Transform filter into spatial domain using U2R()
    % --------------------------------------------------------------------
    filter = U2R(filter);
    
end


